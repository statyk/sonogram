// Generates a test-only Ed25519 keypair as a TS fixture module. NOT for production keys.
import { generateKeyPairSync } from 'node:crypto';

const { publicKey, privateKey } = generateKeyPairSync('ed25519');
const x = publicKey.export({ format: 'jwk' }).x;
const d = privateKey.export({ format: 'jwk' }).d;
const b64 = (s) => s.replace(/-/g, '+').replace(/_/g, '/') + '='.repeat((4 - (s.length % 4)) % 4);

console.log(`// Generated by scripts/gen-test-keys.mjs — test-only keys, not secret.
export const OWNER_JWK_X = '${x}';
export const OWNER_JWK_D = '${d}';
export const OWNER_PUBKEY_B64 = '${b64(x)}';
`);
